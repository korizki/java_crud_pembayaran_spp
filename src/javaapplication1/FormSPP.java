    private void simpanBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanBtnMouseClicked
        DefaultTableModel model = (DefaultTableModel) tabelPembayaran.getModel();        
        // ambil nilai inputan:
        String idSiswa = this.idSiswa.getText();
        String namaSiswa = this.namaSiswa.getText();
        String kelas = this.kelas.getText();
        String jurusan = this.jurusan.getSelectedItem().toString();
        String pembayaran = this.pembayaran.getSelectedItem().toString();
        Integer jumlah = Integer.parseInt(this.jumlah.getText()) ;
        // insert data to table sql
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","");
            Statement insertStatement = connection.createStatement();
            String sql = "INSERT INTO `spp` (`idSiswa`, `namaSiswa`, `kelas`, `jurusan`, `pembayaran`, `jumlah`) VALUES ('"+idSiswa+"','"+namaSiswa+"','"+kelas+"','"+jurusan + "','"+pembayaran+"','"+jumlah+"');";
            insertStatement.executeUpdate(sql);
            GetData();
            JOptionPane.showMessageDialog(null, "Transaksi Berhasil Disimpan / Diupdate");
            clearForm();
        }
        catch (SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Gagal Update / Tambah Pembayaran.");
            clearForm();
            System.err.println(e);
        }
        
        
    }//GEN-LAST:event_simpanBtnMouseClicked

