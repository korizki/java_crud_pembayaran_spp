    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        // TODO add your handling code here:
        if(tabelPembayaran.getSelectedRow() != -1){
            DefaultTableModel model = (DefaultTableModel) tabelPembayaran.getModel();
            try {
                Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","");
                Statement statement = connection.createStatement();
                String idSiswa = model.getValueAt(tabelPembayaran.getSelectedRow(), 0).toString();
                String sql = "SELECT * FROM `spp` WHERE idSiswa = "+idSiswa;
                ResultSet result = statement.executeQuery(sql);
                // get data value and update to input
                String namaSiswa = ""; 
                String kelas = "";
                String jurusan = "";
                String jumlah = "";
                String pembayaran = ""; 
                while (result.next()){
                    namaSiswa=result.getString("namaSiswa");
                    kelas=result.getString("kelas");
                    jurusan=result.getString("jurusan");
                    pembayaran=result.getString("pembayaran");
                    jumlah=result.getString("jumlah");
                }
                // update input
                this.idSiswa.setText(idSiswa);
                this.namaSiswa.setText(namaSiswa);
                this.kelas.setText(kelas);
                this.jumlah.setText(jumlah);
                this.jurusan.setSelectedItem(jurusan);
                this.pembayaran.setSelectedItem(pembayaran);
                result.close();
                statement.close();
                // hapus data dari existing tabel         
                String deleteSql = "DELETE FROM `spp` WHERE idSiswa = "+idSiswa;
                Statement deleteStatement = connection.createStatement();
                deleteStatement.executeUpdate(deleteSql);
                GetData();
            } catch (SQLException | HeadlessException e){
                 JOptionPane.showMessageDialog(null, "Gagal Update / Tambah Pembayaran.");
            }
            //remove old data
            model.removeRow(tabelPembayaran.getSelectedRow());
        }
    }//GEN-LAST:event_updateBtnMouseClicked

