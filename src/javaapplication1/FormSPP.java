    private void GetData(){
        DefaultTableModel model = (DefaultTableModel) tabelPembayaran.getModel();
        model.setRowCount(0);
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/test","root","");
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM spp";
            ResultSet result = statement.executeQuery(sql);
            // load data to jtable
            while (result.next()){
                Object[] object = new Object[6];
                object[0]=result.getString("idSiswa");
                object[1]=result.getString("namaSiswa");
                object[2]=result.getString("kelas");
                object[3]=result.getString("jurusan");
                object[4]=result.getString("pembayaran");
                object[5]=result.getString("jumlah");
                model.insertRow(0, object);
            }
            result.close();
            statement.close();
        }
        catch (SQLException | HeadlessException e){
            
        }
    }
